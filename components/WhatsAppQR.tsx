import { useState, useEffect } from 'react';
import { QRCodeSVG } from 'qrcode.react';
import { MessageCircle, X } from 'lucide-react';

interface WhatsAppQRProps {
  phoneNumber: string;
  message?: string;
  businessName?: string;
}

interface UserData {
  id: number;
  name: string;
  email: string;
}

interface QRRecord {
  id: string;
  userId: number | null;
  userName: string;
  userEmail: string;
  phoneNumber: string;
  message: string;
  businessName: string;
  timestamp: string;
  action: 'viewed' | 'generated' | 'clicked';
}

